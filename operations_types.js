// Create a variable called number1 and store any number in it 
const number1 = 10;

// Create a variable called number2 and store a different number in it
const number2 = 5;

// Convert number1 to a string and store it in a new variable
const numberString = number1.toString();

// Add number1 and number2 and store the results in a variable called sum
const sum = number1 + number2;

// Multiply number1 and number2 and store the result in a variable called product
const product = number1 * number2;

// Print all results to the console
console.log("Number1:", number1);
console.log("Number 2:", number2);
console.log("Number 1 as a string:", numberString);
console.log("Sum:", sum);
console.log("Product:", product);